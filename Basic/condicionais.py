placa = int(input("Digite o primeiro número da sua plca do carro: "))


if (placa == 1 or placa == 2):
    print("Segunda-feira")
elif (placa == 3 or placa == 4):
    print("Terça-feira")
elif (placa == 5 or placa == 6):
    print("Quarta-feira")
elif (placa == 7 or placa == 8):
    print("Quinta-feira")
elif (placa == 0 or placa == 9):
    print("Sexta-feira")
else:
    print("Houve um erro de digitação, por favor, digite novamente!")
